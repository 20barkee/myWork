package review;

public class Part2of4thing {


		private String name;
		   private int grade;
		    
		   public Part2of4thing()
		    {
		     name = "";
		     grade  = 0;
		    }
		    public Part2of4thing(String xName, int xGrade)
		    {
		     name = xName;
		     grade  = xGrade;
		    }
		    public String getName()
		    {
		     return name;
		    }
		    public int getGrade()
		    {
		     return grade;
		    }
		   public void setName(String xName)
		    {
		     name = xName;
		    }
		    public void setGrade(int xGrade)
		    {
		     grade = xGrade;
		    

	}

}
